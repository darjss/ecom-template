const foldUnicode17CodePoint = (codePoint: number) => {
  const expansion = unicode17FullCaseFoldExpansions.get(codePoint);
  if (expansion !== undefined) {
    return expansion;
  }

  const single = unicode17SimpleCaseFoldSingles.get(codePoint);
  if (single !== undefined) {
    return String.fromCodePoint(single);
  }

  for (const [start, end, stride, delta] of unicode17SimpleCaseFoldRanges) {
    if (codePoint >= start && codePoint <= end && (codePoint - start) % stride === 0) {
      return String.fromCodePoint(codePoint + delta);
    }
  }

  return String.fromCodePoint(codePoint);
};

export const fullUnicodeCaseFold = (value: string) =>
  [...value].map((character) => foldUnicode17CodePoint(character.codePointAt(0) ?? -1)).join("");
